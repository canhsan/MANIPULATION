package while_Loop;

public class do_while {
    public static void main(String[] args) {
        /*
        ’k’ harfinden ‘t’ harfine kadar harfleri yazdirin.
         */
        char harf = 'k';
        do {
            System.out.print(harf + " ");
            harf++;

        } while (harf <= 't');

    }

}

